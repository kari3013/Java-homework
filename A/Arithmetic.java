package homework1.A;

public class Arithmetic {
    public static void main(String[] args) {
        //write programs to use all the data types and given arithmetic operations.
        int a=10;
        int b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(b--);
        System.out.println(--b);
    }
}
